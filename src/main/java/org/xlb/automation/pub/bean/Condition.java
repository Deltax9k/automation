package org.xlb.automation.pub.bean;

/**
 * 
 * 条件类
 * @author Lingbo Xie
 * @date 2018-08-29
 * @version V1.2
 *
 */
public class Condition {
	/**
	 * 名称
	 */
	private String name;
	
	/**
	 * 条件类型
	 */
	private String type;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}
	
}
